// Summary:
//     角度转弧度
 float MathUtils::Degree2Radian(float degree)
{
    return PI * degree / 180.0f;
}

// Summary:
//     弧度转角度
 float MathUtils::Radian2Degree(float radian)
{
    return 180.0f * radian / PI;
}

 float MathUtils::DirToAngleFloat(Vector3D vDirD4)
 {
	 if (vDirD4.x > MathUtils::iPointUnit)
	 {
		 vDirD4.x = MathUtils::iPointUnit;
	 }

	 if (vDirD4.x < -MathUtils::iPointUnit)
	 {
		 vDirD4.x = -MathUtils::iPointUnit;
	 }

	 double fRad2Deg = 57.2958f;
	 double fAngle = acos((float)vDirD4.x / MathUtils::floatPointUnit) * fRad2Deg;

	 if (vDirD4.z < 0)
		 fAngle = 360.0f - fAngle;

	 return (float)fAngle;
 }

// Summary:
//     clamp
 int MathUtils::Clamp(int x, int min,  int max)
{
    return x = (x < min) ? min : (x > max ? max : x);
}

